export const V3_CANNED: V3CannedResponse[] = [
  ...DATA_RESPONSES,
  ...V3_STUBS,
  ...JARGON_RESPONSES,
];

export function getCannedById(id: string): V3CannedResponse | undefined {
  return V3_CANNED.find(r => r.id === id);
}

export function getChipsForPage(page: ChatPage, mention?: AgentId): string[] {
  const pool = V3_CANNED.filter(r => {
    if (r.kind === 'jargon') return false;
    if (mention && r.agentId !== mention) return false;
    return r.pages?.includes(page);
  });
  const seen = new Set<string>();
  const chips: string[] = [];
  for (const r of pool) {
    const label = r.question ?? r.chips?.[0];
    if (label && !seen.has(label)) {
      seen.add(label);
      chips.push(label);
    }
  }
  return chips.slice(0, 5);
}
